# Частота события – это число случаев появления возможного 
# события при определенных условиях. 
# Очевидно, что это число f = 0,1,2,3…,n, 
# где f – обозначение частоты, а n – ее максимально возможное значение. 
# Также очевидно, что если f = n, то событие является достоверным, 
# то есть наступает всегда.

# Более точной мерой возможности наступления события 
# является относительная частоты (частость) – p=f/n
# Так как 0≤f≤n, то 0≤p≤1, 
# в данном случае n – общее число наблюдений или испытаний (иногда говорят шансов), 
# а f – число случаев наступления возможного события.

