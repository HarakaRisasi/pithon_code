# Правило суммы. 
# Если некоторый объект X можно выбрать n1 спосо­бами, 
# а объект Y можно выбрать n2 способами, 
# причем первые и вторые способы не пересекаются, 
# то любой из указанных объектов (X или Y), можно выбрать n1 + n2  способами. 

# Пример 2. В студенческой группе 14 девушек и 6 юношей. 
# Сколь­кими способами можно выбрать, для выполнения различных заданий, 
# двух студентов одного пола?

# Решение. 
# По правилу умножения двух девушек можно выбрать 14·13 = 182 
# способами, а двух юношей - 6·5 = 30 способами. 
# Следует выбрать двух студентов одного пола: двух студенток или двух юношей. 
# Соглас­но правилу сложения таких способов выбора будет 182 + 30 = 212 .

# Пример 3.
# Сочетаниями называют различные комбинации из  объектов, 
# которые выбраны из множества  различных объектов, 
# и которые отличаются друг от друга хотя бы одним объектом. 
# Иными словами, отдельно взятое сочетание 
# – это уникальная выборка из элементов, в которой 
# не важен их порядок (расположение). 

# В ящике находится 15 деталей. Сколькими способами можно взять 4 детали?
# Формула общего колличества сочетаний
#  m         n!
# C == _____________   or Cmn = n! / (n - m)! * m!
#  n   (n - m)! * m!

#  4        15!
# C == ______________ or C(4; 15) = 15! / (15 - 4)! * 4!
#  15  (15 - 4)! * 4!

import math

print (int(math.factorial(15) / (math.factorial(15-4) * math.factorial(4))))
# 1365

